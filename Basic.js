/*  Q1: What is a variable? */

//  Ans : A variable points to a specific memory address that stores a value. Variables are given a name which can be used throughout your code to access that value.

/*  Q2: Variable Declaration */

// Ans: Declaring a variable means giving it a name. In JavaScript, this is often done with the let keyword.

/*Rules for Variable declaration */

// Variable naming follows specific rules:
//  names can include letters, numbers,
//  dollar signs, and underscores,
//  but cannot contain spaces and must not begin with a number.

// example : let character;

/* Q3: What is initialization */

// Ans:Assigning a value to a variable at the moment of its declaration is known as initialization.

// example: let character = "Hello";

// truthy and falsy values

// A truthy value is a value that is considered true when evaluated as a boolean. Most of the values you encounter in JavaScript will be truthy.

// A falsy value is the opposite - a value considered false when evaluated as a boolean. JavaScript has a defined list of falsy values. Some of them include false, 0, "", null, undefined, and NaN.
